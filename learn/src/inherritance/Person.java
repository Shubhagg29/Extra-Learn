package inherritance;

public class Person {
	
	
	protected String name;
	
	public void eat()
	{
		System.out.println(name + "Person is eating !!");
	}

	public void see()
	{
		System.out.println(name +" is seeing  !!"  );
	}
	public void walk()
	{
		System.out.println("Person is walking !!");
	}
}
